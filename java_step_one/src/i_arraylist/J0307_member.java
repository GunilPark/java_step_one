package i_arraylist;

public class J0307_member {
	private String name, area;
	private int age;
	public J0307_member(String name, String area, int age) {
		this.name = name;
		this.area = area;
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public String getArea() {
		return area;
	}
	public int getAge() {
		return age;
	}
	
	
	
}
